<!-- Script Of Modal -->

<script>
    $(document).ready(function() {
        // Event listener for when the modal is shown
        $('#addToCartModal').on('shown.bs.modal', function (e) {
            // Get data from the clicked button
            var productID = $(e.relatedTarget).data('productid');
            var customerID = $(e.relatedTarget).data('customerid');
            var page = $(e.relatedTarget).data('page');

            // Redirect to addToCart.php with parameters after a short delay (1 second)
            setTimeout(function() {
                window.location.href = 'includes/addToCart.php?ProductID=' + productID +
                                      '&CustomerID=' + customerID +
                                      '&page=' + page;
            }, 1000);
        });
    });
</script>


